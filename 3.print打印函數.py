######################換行關鍵字\n######################

#範例1:使輸入的游標(cursor)跳到下一行
input("你叫甚麼名字?\n")

#範例2:使\n後的字串跳到下一行
print("你最喜歡的\nNBA球員是誰?")


######################連接多個字串######################

#範例1:把兩個獨立的字串連接起來

a="你知道"
b="我是誰嗎?"
print(a+b)

######################連接多個變數######################

#範例1:
location = input("你住哪裡啊? ")
print(f"Wow,我也住在{location}耶!")

#範例2:
c="我最喜歡的號碼是"
d = 34
print(f"{c}{d}")